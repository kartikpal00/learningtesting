package exercise_11_Switch;

public class Lab095_interview {
    public static void main(String[] args) {
        char code = 'c';

        switch (code){
            default:
                System.out.println("hellooooo");
                break;
            case 'a':
                System.out.println("65");
                break;
            case 'b':
                System.out.println("66");
                break;
        }
    }
}
