package exercise_13_while_loop;

public class Lab113_while_loop {
    public static void main(String[] args) {
        int i = 0;
        while (i<10){
            System.out.println(i);
            i++;
        }

        // I  ,  C  ,   U
        int j = 0;
        for (; j<10 ; j++){
            System.out.println(j);
        }


    }
}
