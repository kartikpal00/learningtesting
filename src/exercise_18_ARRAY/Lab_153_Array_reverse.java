package exercise_18_ARRAY;

public class Lab_153_Array_reverse {
    public static void main(String[] args) {
        int[] number  = {1,2,3,4,5,6};
        // 6,5,4,3,2,1
        for (int i = number.length-1; i >=0; i--) {
            System.out.println(number[i]);
            
        }
        
    }
}
