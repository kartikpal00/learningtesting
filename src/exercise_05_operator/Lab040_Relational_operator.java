package exercise_05_operator;

public class Lab040_Relational_operator {
    public static void main(String[] args) {
        //relational operator

        // < less than
        // < = -> less than or equal than
        // > -> greater
        // > = greater or equal
        // == equal to ( but checking )
        // ! = -> not equal

        // all of then will result boolean output .
        int a = 10;
        int b = 30;
        boolean c = a> b; // 10>30
        System.out.println(c);

        int age_mamta = 33;
        int age_pramod = 34;
        boolean result = age_pramod >= age_mamta;
        System.out.println(result);
    }
}
